package es.grammata.evaluation.evs.mvc.controller.util;

public class EvaluationReport {
	
	private String codigoMateria;
	private String nombreMateria;
	private String origenEvaluacion;
	private String nombreTest;
	private String tipoEvaluacion;
	private String habilitadoPublicacion;
	private String nExamenes;
	private String examenesCalificados;
	private String examenesPublicados;
	private String porcentajePublicacion;
	
	public String getCodigoMateria() {
		return codigoMateria;
	}
	public void setCodigoMateria(String codigoMateria) {
		this.codigoMateria = codigoMateria;
	}
	public String getNombreMateria() {
		return nombreMateria;
	}
	public void setNombreMateria(String nombreMateria) {
		this.nombreMateria = nombreMateria;
	}
	public String getOrigenEvaluacion() {
		return origenEvaluacion;
	}
	public void setOrigenEvaluacion(String origenEvaluacion) {
		this.origenEvaluacion = origenEvaluacion;
	}
	public String getTipoEvaluacion() {
		return tipoEvaluacion;
	}
	public void setTipoEvaluacion(String tipoEvaluacion) {
		this.tipoEvaluacion = tipoEvaluacion;
	}
	public String getHabilitadoPublicacion() {
		return habilitadoPublicacion;
	}
	public void setHabilitadoPublicacion(String habilitadoPublicacion) {
		this.habilitadoPublicacion = habilitadoPublicacion;
	}
	public String getNExamenes() {
		return nExamenes;
	}
	public void setNExamenes(String nExamenes) {
		this.nExamenes = nExamenes;
	}
	public String getExamenesPublicados() {
		return examenesPublicados;
	}
	public void setExamenesPublicados(String examenesPublicados) {
		this.examenesPublicados = examenesPublicados;
	}
	public String getPorcentajePublicacion() {
		return porcentajePublicacion;
	}
	public void setPorcentajePublicacion(String porcentajePublicacion) {
		this.porcentajePublicacion = porcentajePublicacion;
	}
	public String getNombreTest() {
		return nombreTest;
	}
	public void setNombreTest(String nombreTest) {
		this.nombreTest = nombreTest;
	}
	public String getnExamenes() {
		return nExamenes;
	}
	public void setnExamenes(String nExamenes) {
		this.nExamenes = nExamenes;
	}
	public String getExamenesCalificados() {
		return examenesCalificados;
	}
	public void setExamenesCalificados(String examenesCalificados) {
		this.examenesCalificados = examenesCalificados;
	}
	
}
